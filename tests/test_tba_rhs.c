static int compare_vectors(const char* label, const sunrealtype* c_vals,
                           const sunrealtype* f_vals, int n, double tol)
{
  int nmismatch = 0;
  double maxrel = 0.0;
  int maxidx = -1;
  for (int i = 0; i < n; i++) {
    double fv = f_vals[i], cv = c_vals[i];
    double scale = fabs(fv) > 1.0 ? fabs(fv) : 1.0;
    double rel = fabs(cv - fv) / scale;
    if (rel > tol) {
      if (nmismatch < 10)
        printf("  %s(%3d): F=%23.15e  C=%23.15e  reldiff=%.3e\n",
               label, i+1, fv, cv, rel);
      nmismatch++;
    }
    if (rel > maxrel) { maxrel = rel; maxidx = i; }
  }
  if (nmismatch > 10)
    printf("  ... and %d more mismatches\n", nmismatch - 10);
  printf("  %s: %d/%d mismatches (tol=%.0e), max reldiff=%.3e at index %d\n",
         label, nmismatch, n, tol, maxrel, maxidx+1);
  return nmismatch;
}

